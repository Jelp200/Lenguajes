#**
#**     "chmod" (change mode) nos permite cambiar los permisos de archivos o directorios, esto para poder
#**     controlar quien puede leer, escribir o ejecutar el archivo.
#**
#**     Su sintaxis de chmod es:
#**         chmod [opcion] [modo] [archivo]
#**         Siendo:
#**             [opcion]:
#**                + : Añade permisos.
#**                - : Remueve permisos.
#**                = : Coloma permisos.
#**            [modo]:
#**                r : Permiso de lectura.
#**                w : Permiso de escritura.
#**                x : Permiso de ejecución.
